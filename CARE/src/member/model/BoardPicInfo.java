package member.model;

public class BoardPicInfo {
	private int boardNo;
	private String boardPic1;

	public BoardPicInfo(String boardPic1) {
		this.boardPic1 = boardPic1;
	}

	public int getBoardNo() {
		return boardNo;
	}

	public void setBoardNo(int boardNo) {
		this.boardNo = boardNo;
	}

	public String getBoardPic1() {
		return boardPic1;
	}

	public void setBoardPic1(String boardPic1) {
		this.boardPic1 = boardPic1;
	}
	
	
}
